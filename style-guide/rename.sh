#!/bin/zsh
swap-name() {
for f in $@; do
	mv $f `echo $f | sed "s/\([a-z]*\)-\([1-9].[1-9].[1-9a-z]*\)/\2-\1/"`
done;
}

fix-numeral() {
for f in $@; do
	mv $f `echo $f | sed -e "s/\.i\([.-]\)/.1\1/"       \
		             -e "s/\.ii\([.-]\)/.2\1/"      \
		             -e "s/\.iii\([.-]\)/.3\1/"     \
		             -e "s/\.iv\([.-]\)/.4\1/"      \
		             -e "s/\.v\([.-]\)/.5\1/"       \
		             -e "s/\.vi\([.-]\)/.6\1/"      \
		             -e "s/\.vii\([.-]\)/.7\1/"     \
		             -e "s/\.viii\([.-]\)/.8\1/"    \
		             -e "s/\.ix\([.-]\)/.9\1/"      \
		             -e "s/\.x\([.-]\)/.10\1/"      \
		             -e "s/\.xi\([.-]\)/.11\1/"     \
		             -e "s/\.xii\([.-]\)/.12\1/"    \
		             -e "s/\.xiii\([.-]\)/.13\1/"   \
		             -e "s/\.xiv\([.-]\)/.14\1/"    \
		             -e "s/\.xv\([.-]\)/.15\1/"     `
done;
}
